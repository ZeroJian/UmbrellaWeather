import UIKit

extension UIColor {
  
  class func rainColor() -> UIColor {
    return UIColor(red: 98/255, green: 203/255, blue: 238/255, alpha: 1)
  }
  
  class func shallowBlack() -> UIColor {
    return UIColor(red: 32/255, green: 32/255, blue: 41/255, alpha: 1)
  }
  
  class func GreenBlue() -> UIColor {
  return UIColor(red: 21/255, green: 167/255, blue: 230/255, alpha: 1)
  }
}